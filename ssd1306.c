const int font_w = 6;
const int ascii_offset = 32;
int i2c_packet_offset;

static void add_number(int number, int base)
{
    const char* format = "0123456789abcdef";
    char buffer[16];
    unsigned int offset;
    unsigned int font_idx;

    offset = sizeof(buffer) - 1;

    do
    {
        buffer[offset--] = format[number % base];
        number /= base;
    } while (number > 0);

    offset++;

    while(offset < sizeof(buffer))
    {
        font_idx = buffer[offset++] - ascii_offset;
        memcpy(i2c_packet + i2c_packet_offset, font6x8[font_idx], font_w);
        i2c_packet_offset += font_w;
    }
}

void ssd1306_printf(uint8_t address, const char* fmt, ...)
{
    int font_idx;
    int n;

    va_list ptr;
    va_start(ptr, fmt);

    i2c_packet[0] = SSD1306_CTRL_BYTE_DATA;
    i2c_packet_offset = 1;

    while (*fmt != '\0')
    {
        if (*fmt != '%')
        {
            font_idx = *fmt - ascii_offset;
            memcpy(i2c_packet + i2c_packet_offset, font6x8[font_idx], font_w);
            i2c_packet_offset += font_w;
            fmt++;
            continue;
        }

        fmt++;

        switch(*fmt)
        {
            case 'd':
                n = va_arg(ptr, int);
                add_number(n, 10);
                fmt++;
                break;
            case 'x':
                n = va_arg(ptr, int);
                add_number(n, 16);
                fmt++;
                break;
            case 'c':
                n = va_arg(ptr, int);
                font_idx = n - ascii_offset;
                memcpy(i2c_packet + i2c_packet_offset, font6x8[font_idx], font_w);
                i2c_packet_offset += font_w;
                fmt++;
                break;
            default:
                break;
        }
    }
    va_end(ptr);
    ssd1306_i2c_write(address, i2c_packet, i2c_packet_offset + 1);
}
